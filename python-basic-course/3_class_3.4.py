# List joining
# list joining with '+' operator and extend() method

num_list = [10, 20, 30]
name_list = ["Rohim", "Korim", "Khalek"]

# join with '+' operator
joined_list = num_list + name_list
print(joined_list)

# join with extent() method
num_list.extend(name_list)
print(num_list)

