# OOP ( Object Oriented Programming ) Recap
# Abstraction
# Encapsulation
# Inheritance
# Polymorphism

class Cat:
	name = ""
	age = 0
	color = ""

cat1 = Cat()
cat1.name = "Tom"
cat1.age = 7
cat1.color = "Blue"

cat2 = Cat()
cat2.name = "Helsink"
cat2.age = 70
cat2.color = "Red"


print(f"{cat1.name} is {cat1.age} years old, it's color is {cat1.color}")
print(f"{cat2.name} is {cat2.age} years old, it's color is {cat2.color}")